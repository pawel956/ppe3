    private void jButtonChoisirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChoisirActionPerformed
        if (this.jFileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            this.choix = this.jFileChooser.getSelectedFile();
            jTextFieldChemin.setText(this.choix.getAbsolutePath());
            choixDossierOK = true;
        }
    }//GEN-LAST:event_jButtonChoisirActionPerformed

    private void jButtonValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValiderActionPerformed
        if (buttonGroup.getSelection() != null) {
            this.formatFichier = buttonGroup.getSelection().getActionCommand();
        } else {
            this.formatFichier = null;
        }

        if (buttonGroup1.getSelection() != null) {
            this.donneesAExporter = buttonGroup1.getSelection().getActionCommand();
        } else {
            this.donneesAExporter = null;
        }

        if (this.getDonneesAExporter() != null) {
            if (!this.getChoixDossierOK() && this.getFormatFichier() == null) {
                jLabelEtat.setForeground(Color.red);
                jLabelEtat.setText("Choix du dossier de destination et format du fichier manquants");
            } else if (!this.getChoixDossierOK() && this.getFormatFichier() != null) {
                jLabelEtat.setForeground(Color.red);
                jLabelEtat.setText("Choix du dossier de destination manquant");
            } else if (this.getChoixDossierOK() && this.getFormatFichier() == null) {
                jLabelEtat.setForeground(Color.red);
                jLabelEtat.setText("Choix du format du fichier manquant");
            }
        } else {
            jLabelEtat.setForeground(Color.red);
            jLabelEtat.setText("Choix des données à exporter manquant");
        }
    }//GEN-LAST:event_jButtonValiderActionPerformed

