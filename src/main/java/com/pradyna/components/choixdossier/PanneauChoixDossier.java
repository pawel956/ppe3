    private void updateButtonGroup1() {
        if (buttonGroup1.getSelection() != null) {
            jRadioButtonJSON.setEnabled(true);
            jRadioButtonXML.setEnabled(true);
            jRadioButtonCSV.setEnabled(true);

            String actionCommand = buttonGroup1.getSelection().getActionCommand();

            if (actionCommand.equals("Tout")) {
                jRadioButtonPDF.setEnabled(true);
            } else {
                jRadioButtonPDF.setEnabled(false);
            }
        }
    }


